public class PSVs {
    private int idPSV;
    private String nomePSV;
    private String descricaoPSV;

    public int getIdPSV() {
        return idPSV;
    }

    public void setIdPSV(int idPSV) {
        this.idPSV = idPSV;
    }

    public String getNomePSV() {
        return nomePSV;
    }

    public void setNomePSV(String nomePSV) {
        this.nomePSV = nomePSV;
    }

    public String getDescricaoPSV() {
        return descricaoPSV;
    }

    public void setDescricaoPSV(String descricaoPSV) {
        this.descricaoPSV = descricaoPSV;
    }
}
